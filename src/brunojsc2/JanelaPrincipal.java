    private void BtnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnOKActionPerformed
        
        String nome = txtNome.getText();
        String mensagem = "Caro " + nome + " VocÃª gosta de: ";
        
        if(chkRock.isSelected()){
        
        mensagem = mensagem +"Rock ";
        }
          if(chkPagode.isSelected()){
        
        mensagem = mensagem +"Pagode ";
        }
            if(chkMPB.isSelected()){
        
        mensagem = mensagem +"MPB ";
        }
              if(chkPop.isSelected()){
        
        mensagem = mensagem +"Pop ";
        }
                if(chkSertanejo.isSelected()){
        
        mensagem = mensagem +"Sertanejo ";
        }
                
           if(chkOutros.isSelected()){
        
        mensagem = mensagem +"Outros ";
        }
        
        jLabel1.setText(mensagem);
    }//GEN-LAST:event_BtnOKActionPerformed

